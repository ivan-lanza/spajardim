                <h3 class="green-title">Tratamentos: Maquiagem definitiva</h3>
                
                <p class="text-justify">
                    A Micropigmenta&ccedil;&atilde;o est&eacute;tica e param&eacute;dica, popularmente conhecida como Maquiagem Definitiva, foram criadas visando corrigir, embelezar e criar harmonia na face de forma semi-permanente.
                    <br><br>
                    O trabalho &eacute; realizado com uma micro-agulha e uma ponteira descart&aacute;vel, capaz de desenhar tra&ccedil;os bem naturais, criando o melhor formato em harmonia com a face.<br>
                    Homens podem e devem fazer a sobrancelha definitiva, com o prop&oacute;sito de corrigir falhas e cicatrizes.
                    <br><br>
                    <strong>Mitos e verdades</strong>
                </p>
                <h4 class="green-title">Maquiagem definitiva &eacute; uma tatuagem?</h4>
                <p class="text-justify">
                    N&atilde;o. Existem muitas diferen&ccedil;as. A maior diferen&ccedil;a est&aacute; na profundidade que se introduz o pigmento na pele.
                    <br><br>
                    &eacute; um procedimento semelhante &agrave; tatuagem, mas as t&eacute;cnicas e materiais s&atilde;o totalmente distintos.
                </p>
                
                <h4 class="green-title">Na tatuagem se introduz o pigmento na pele de forma profunda e permanente. <br><br>Na micro-pigmenta&ccedil;&atilde;o se introduz o pigmento na pele de forma menos profunda e semi-permanente. Por que?</h4>
                <p class="text-justify">
                    Para que o pigmento possa ir desbotando na pele, e com o passar do tempo desapare&ccedil;a totalmente. Sabemos que o envelhecimento &eacute; 
inevit&aacute;vel, e a lei da gravidade exerce sua a&ccedil;&atilde;o, por isso a maquiagem deve ser semi-permanente.
                </p>
                <br>
                
                <h4 class="green-title">Etapas</h4>
                <p class="text-justify">
                    &#8226; Iniciamos com uma avalia&ccedil;&atilde;o<br>
                    &#8226; Estudamos a forma do rosto e a cor<br>
                    &#8226; Desenhamos a sobrancelha ou l&aacute;bios, olhos, ar&eacute;olas<br>
                    &#8226; Primeira sess&atilde;o de maquiagem<br>
                    &#8226; Retocar ap&oacute;s 30 dias (segunda sess&atilde;o)
                </p>
                
                <h4 class="green-title">Ap&oacute;s 15 dias do retoque, retornar para verificar se est&aacute; tudo correndo bem!</h4>
                <p class="text-justify">
                    &#8226;  Revis&atilde;o ap&oacute;s 1 ano<br>
                    &#8226; Renova&ccedil;&atilde;o ap&oacute;s 1 ano e meio<br>
                    &#8226; Reestrutura&ccedil;&atilde;o ap&oacute;s 2 anos<br>
                    &#8226; Reconstru&ccedil;&atilde;o ap&oacute;s 3 anos<br>
                    <br>
                    Algumas maquiagens sofrem altera&ccedil;&otilde;es da cor e tempo de dura&ccedil;&atilde;o, isso ocorre em fun&ccedil;&atilde;o do estilo de vida: Sol, praia, uso de &aacute;cidos. Por esse motivo deve ser feita a revis&atilde;o anual.
                </p>
                
                <h4 class="green-title">Cuidados p&oacute;s-pigmentos: Evitar durante 5 dias</h4>
                <p class="text-justify">
                    &#8226; Nata&ccedil;&atilde;o, sauna, praia, gin&aacute;stica e sol;<br>
                    &#8226; Fazer higiene di&aacute;ria com soro fisiol&oacute;gico/pomada;<br>
                    &#8226; N&atilde;o molhar durante o banho com &aacute;gua, ou com qualquer outro produto;<br>
                    &#8226; Evitar alimentos picantes, bem como: Mariscos, frutos do mar e frutas &aacute;cidas.
                </p>
                
